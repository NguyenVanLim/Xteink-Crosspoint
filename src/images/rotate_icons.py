from PIL import Image

def rotate_icon_data(data_bytes, width, height, rotation=-90):
    """Rotate 1-bit bitmap data."""
    bits = []
    for byte in data_bytes:
        for i in range(7, -1, -1):
            bits.append((byte >> i) & 1)
    
    pixels = []
    bit_idx = 0
    for y in range(height):
        row = []
        for x in range(width):
            if bit_idx < len(bits):
                row.append(bits[bit_idx])
            else:
                row.append(1)
            bit_idx += 1
        pixels.append(row)
        if width % 8 != 0:
            bit_idx += (8 - (width % 8))
    
    if rotation == -90:
        rotated = [[pixels[x][height - 1 - y] for x in range(height)] for y in range(width)]
        new_w, new_h = height, width
    else:
        return data_bytes, width, height
    
    result = []
    byte = 0
    bit_count = 0
    
    for y in range(new_h):
        for x in range(new_w):
            bit = rotated[y][x]
            byte = (byte << 1) | bit
            bit_count += 1
            
            if bit_count == 8:
                result.append(byte)
                byte = 0
                bit_count = 0
        
        if bit_count > 0:
            byte = byte << (8 - bit_count)
            result.append(byte)
            byte = 0
            bit_count = 0
    
    return result, new_w, new_h

